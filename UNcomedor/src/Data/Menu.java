package Data;
public class Menu {

    private String postre;
    private String sopa;
    private String jugo;
    private String proteina;
    private String principio;
    private String carbohidrato;

    public Menu(String postre,String sopa,String jugo,String proteina,String principio,String carbohidrato){
        this.postre = postre;
        this.sopa = sopa;
        this.jugo = jugo;
        this.proteina = proteina;
        this.principio = principio;
        this.carbohidrato = carbohidrato;
    }
    
}
